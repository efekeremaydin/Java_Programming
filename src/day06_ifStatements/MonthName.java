package day06_ifStatements;

public class MonthName {

    public static void main(String[] args) {

        int number = 1;

        String result = "";

        if (number == 1){
            result = "Jan";
        }

        if (number == 2){
            result = "Feb";
        }

        if (number == 3){
            result = "Mar";
        }

        if (number == 4){
            result = "Apr";
        }

        if (number == 5){
            result = "May";
        }

        if (number == 6){
            result = "Jun";
        }

        if (number == 7){
            result = "Jul";
        }

        if (number == 8){
            result = "Aug";
        }

        if (number == 9){
            result = "Sep";
        }

        if (number == 10){
            result = "Oct";
        }

        if (number == 11){
            result = "Nov";
        }

        if (number == 12){
            result = "Dec";
        }

        System.out.println(result);
    }

}

