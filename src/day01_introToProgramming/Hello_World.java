package day01_introToProgramming;

public class Hello_World { // This is a class header

    public static void main(String[] args) { // This is the main method. you put these {} in order to define the scope. args can be any name. String is a class, that's why it's upper case.

        System.out.println("Hello World"); // This is a print statement. It prints and appends a new line. whatever you write it has to be in double quote. you can write as many space as you want, it won't be printed (before "). this always has to be in the main method and in the class.
        System.out.println("How are you?");

    }

}

