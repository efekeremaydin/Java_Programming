package day15_whileLoop;

public class WhileLoopIntro {

    public static void main(String[] args) {

        if (true){
            System.out.println("Hello World"); // one time
        }


        System.out.println("---");

        for (int i = 0; i < 10; i++) { // you can tell the end point for for loop

        }


        while (true){ // repeated if statement // there's no end point
            System.out.println("Hello Cydeo"); // inf times
            break;
        }



    }
}
