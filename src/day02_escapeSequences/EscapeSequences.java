package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class"); // new line \n

        System.out.println("------------------------------------------------");

        System.out.println("\tJava Programming Language"); // Tab \t
        System.out.println("\t\tJava Programming Language");

        System.out.println("------------------------------------------------");

        System.out.println("\\"); // \\ prints one backslash

        System.out.println("------------------------------------------------");

        System.out.println("My favourite TV show is \"The Gonul Dagi\""); // \" double quote


    }
}

/*
1. \n: new line
2. \t: paragraph space
3. \\: backslash
4. \": double quote
 */
